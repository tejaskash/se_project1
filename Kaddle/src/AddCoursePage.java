    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTextField1.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Please Enter AN Activity Title ");
        } else if (jTextField2.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Please Enter The Capacity ");
        } else if (jTextField3.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Please Enter The Cost ");
        } else if (jTextField5.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Please Enter The Duration of the Course ");
        } else if (!(jRadioButton1.isSelected()||jRadioButton2.isSelected())) {
            JOptionPane.showMessageDialog(this, "Please Enter The Eligibilty Criteria of the Course ");
        }
        else if (jTextField4.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Please Enter The Course ID ");
        }
        else {
            try {
                String elg = "";
                if (jRadioButton1.isSelected()) {
                    elg = "OnlyAdults";
                } else if (jRadioButton2.isSelected()) {
                    elg = "Both";
                }
                Class.forName("java.sql.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/kaddle", "root", "");
                Statement stmt = con.createStatement();
                String q1 = "insert into activity values('" + jTextField4.getText() + "','" + jTextField1.getText() + "','" + uid + "','" + jTextField2.getText() + "','" + jComboBox1.getSelectedItem() + "','" + elg + "','" + jTextField3.getText() + "','" + jTextField5.getText() + "');";
                int n = stmt.executeUpdate(q1);
                JOptionPane.showMessageDialog(this, n + " New Activity Has Been Successfully Added", "Success", JOptionPane.INFORMATION_MESSAGE);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                //jTextField6.setText("");
                jComboBox1.setSelectedIndex(0);
                //jComboBox2.setSelectedIndex(0);
                //jComboBox3.setSelectedIndex(0);
                buttonGroup1.clearSelection();
                jTextField1.grabFocus();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                //jTextField6.setText("");
                jComboBox1.setSelectedIndex(0);
                //jComboBox2.setSelectedIndex(0);
                //jComboBox3.setSelectedIndex(0);
                buttonGroup1.clearSelection();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

