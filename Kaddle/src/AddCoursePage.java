    private void jTextField4FocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField4FocusGained
        // TODO add your handling code here:

        //Code for automatic generation of activity_id below:
        try {
            Class.forName("java.sql.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/Kaddle", "root", "");
            Statement stmt = con.createStatement();
            String q1 = "select max(Act_id) from activity;";
            ResultSet rs = stmt.executeQuery(q1);
            rs.next();
            if (!(rs.getString(1) == null)) {
                int no = Integer.parseInt(rs.getString(1)) + 1;
                jTextField4.setText(Integer.toString(no));
            } else {
                jTextField4.setText("5001");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTextField4FocusGained

    private void jTextField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyTyped
        // TODO add your handling code here:

        char ch = evt.getKeyChar();
        if (!(Character.isDigit(ch)) || ch == KeyEvent.VK_PERIOD) {
            evt.consume();
        }
        String st = jTextField2.getText();
        int l = st.length();
        if (l >= 5) {
            evt.consume();
            JOptionPane.showMessageDialog(this, "Maximum 9999 Participants Allowed");
        }
    }//GEN-LAST:event_jTextField2KeyTyped

